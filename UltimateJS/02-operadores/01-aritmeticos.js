let a = 5;
let b = 7;

console.log(a + b); //adición o suma
console.log(a - b); //resta
console.log(a * b); //multiplicacion
console.log(a / b); //division
console.log(a % b); //módulo (Es lo que sobra. Si la division es exacta da 0)
console.log(a ** b); //potencia
console.log(" ");
//incrementar
console.log(++a);
console.log(a++);
console.log(a);

//decrementar
console.log(--a);
console.log(a--);
console.log(a);
